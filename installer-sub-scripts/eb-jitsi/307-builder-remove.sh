# ------------------------------------------------------------------------------
# BUILDER-REMOVE.SH
# ------------------------------------------------------------------------------
set -e
source $INSTALLER/000-source

# ------------------------------------------------------------------------------
# ENVIRONMENT
# ------------------------------------------------------------------------------
MACH="$TAG-builder"
cd

# ------------------------------------------------------------------------------
# INIT
# ------------------------------------------------------------------------------
[[ "$DONT_REMOVE_BUILDER" = true ]] && exit

echo
echo "---------------------- REMOVE BUILDER ---------------------"

# ------------------------------------------------------------------------------
# REMOVING
# ------------------------------------------------------------------------------
# remove the container if exists
set +e
lxc-stop -n $MACH
lxc-wait -n $MACH -s STOPPED
lxc-destroy -n $MACH
rm -rf /var/lib/lxc/$MACH
set -e
